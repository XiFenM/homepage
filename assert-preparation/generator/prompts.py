"""
Prompt definitions for Wave 1+ asset generation.

Each entry corresponds to an asset code (S1, S2A, S2B, ...) defined in
../prompts/static/S{n}-*.md. The markdown files are the human-readable
specs; this file is the machine-executable source of truth.

When updating a prompt: edit BOTH this file AND the corresponding .md.
"""

from dataclasses import dataclass


@dataclass(frozen=True)
class AssetSpec:
    code: str                  # e.g. "S1", "S2A", "S2B"
    label: str                 # short human label
    prompt: str                # the prompt sent to gpt-image-2
    image_size: str            # "WxH", must be multiples of 16, ≤3840 edge
    n: int = 4                 # number of candidates per call
    output_mime: str = "image/png"


